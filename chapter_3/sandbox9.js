// switch statements
const grade = 'P';

switch(grade){
  case 'A':
      console.log('you got and A!');
      break;
  case 'B':
      console.log('you got and B!');
      break;
  case 'C':
      console.log('you got and C!');
      break;
  case 'D':
      console.log('you got and D!');
      break;
  case 'F':
      console.log('you got and F!');
      break;
  default:
      console.log('not a valid grade');
}
